public class FileLine {

    private int line;           //行数
    private String fileLine;    //行内容
    private int fileLineLength; //行内字符数量

    public int getLine() {
        return line;
    }

    public void setLine(int line) {
        this.line = line;
    }

    public String getFileLine() {
        return fileLine;
    }

    public void setFileLine(String fileLine) {
        this.fileLine = fileLine;
    }

    public int getFileLineLength() {
        return fileLineLength;
    }

    public void setFileLineLength(int fileLineLength) {
        this.fileLineLength = fileLineLength;
    }
}
