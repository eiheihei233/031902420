public class MaskWords {

    private String maskWords;  //屏蔽字内容
    private int maskWordsLength; //屏蔽字长度

    public String getMaskWords() {
        return maskWords;
    }

    public void setMaskWords(String maskWords) {
        this.maskWords = maskWords;
    }

    public int getMaskWordsLength() {
        return maskWordsLength;
    }

    public void setMaskWordsLength(int maskWordsLength) {
        this.maskWordsLength = maskWordsLength;
    }
}
